public class MathOperation {
    static void main(String[] args) {

        int a = 100;
        int b = 200;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(b / b);
        System.out.println(a % b);

        int c = 100;

        c += 20;
        System.out.println(c);

        c = c - 20;
        System.out.println(c);

        c *= 2;
        System.out.println(c);

        int d = 100;
        int e = -200;

        ++d;
        System.out.println(d);
        --d;
        System.out.println(d);



    }
}
